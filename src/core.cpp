static const int FONT_FIRST = 32;
static const int FONT_LAST  = 90;

static void draw_char(SDL_Renderer* r, char c, int x, int y, int scale) {
    int idx = (int)(unsigned char)c - FONT_FIRST;
    if (idx < 0 || idx > FONT_LAST - FONT_FIRST) return;
    const Uint8* glyph = bmp_font[idx];
    for (int row = 0; row < 8; row++) {
        for (int col = 0; col < 8; col++) {
            if (glyph[row] & (0x80u >> col)) {
                SDL_Rect px = { x + col * scale, y + row * scale, scale, scale };
                SDL_RenderFillRect(r, &px);
            }
        }
    }
}

// Draw a string. Characters outside the supported range are skipped (treated as space).
// Scale controls pixel size: scale=1 → 8px tall, scale=2 → 16px tall.
void draw_text(SDL_Renderer* ren, const char* text, int x, int y, int scale,
               Uint8 r, Uint8 g, Uint8 b) {
    int cw = 8 * scale;
    // Shadow one pixel down-right
    SDL_SetRenderDrawColor(ren, 0, 0, 0, 255);
    for (int i = 0; text[i]; i++)
        draw_char(ren, text[i], x + 1 + i * cw, y + 1, scale);
    // Main colour
    SDL_SetRenderDrawColor(ren, r, g, b, 255);
    for (int i = 0; text[i]; i++)
        draw_char(ren, text[i], x + i * cw, y, scale);
}

// Returns the pixel width of a string at the given scale.
int text_width(const char* text, int scale) {
    return (int)strlen(text) * 8 * scale;
}

void draw_nes_panel(SDL_Renderer* ren, int x, int y, int w, int h) {
    SDL_SetRenderDrawColor(ren, 255, 255, 255, 255);
    SDL_Rect outer = {x, y, w, h};
    SDL_RenderFillRect(ren, &outer);
    SDL_SetRenderDrawColor(ren, 0, 0, 0, 255);
    SDL_Rect inner = {x + 4, y + 4, w - 8, h - 8};
    SDL_RenderFillRect(ren, &inner);
}

void draw_fps(SDL_Renderer* renderer, float dt) {
    static float acc         = 0.0f;
    static int   frames      = 0;
    static int   display_fps = 0;

    acc += dt;
    frames++;
    if (acc >= 0.5f) {
        display_fps = (int)(frames / acc + 0.5f);
        frames = 0;
        acc    = 0.0f;
    }

    char buf[16];
    SDL_snprintf(buf, sizeof(buf), "FPS:%d", display_fps);
    draw_text(renderer, buf, 4, 4, 2, 255, 220, 0);
}

double time_delta_seconds(void) {
    static Uint64 prev = 0;

    if (prev == 0) {
        prev = SDL_GetPerformanceCounter();
        return 0.0;
    }

    Uint64 now = SDL_GetPerformanceCounter();
    double dt = (double)(now - prev) / (double)SDL_GetPerformanceFrequency();
    prev = now;
    return dt;
}
